package firstweek;

public class Student {
	private String name;
	private int age;
	private char gender;
	
	public void setName(String name){
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public char getGender() {
		return gender;
	}

	public void setGender(char gender) {
		this.gender = gender;
	}

	public String getName(){
		return name;
	}
}
